import type { UseArticleListViewPropsParams, UseArticleListViewPropsResult } from "./article-list.types";
import { useArticleListBodyProps } from "./use-article-list-body-props";

export function useArticleListViewProps({
  t,
  tc,
  layoutMode,
  showSearch,
  searchQuery,
  searchInputRef,
  showSidebarButton,
  sidebarButtonLabel,
  sidebarButtonText,
  isSidebarVisible,
  feedModeControl,
  handleMarkAllRead,
  handleSidebarToggle,
  handleToggleSearch,
  handleCloseSearch,
  setSearchQuery,
  contextStripContext,
  listRef,
  viewportRef,
  handleListKeyDownCapture,
  isLoading,
  isLoadingAccountArticles,
  isLoadingTagArticles,
  isSearchLoading,
  isSearchEmptyState,
  trimmedDebouncedQuery,
  articleGroups,
  dimArchived,
  textPreview,
  imagePreviews,
  selectionStyle,
  selectArticle,
  effectiveViewMode,
  footerModes,
  setViewMode,
}: UseArticleListViewPropsParams): UseArticleListViewPropsResult {
  const bodyProps = useArticleListBodyProps({
    t,
    tc,
    listRef,
    viewportRef,
    handleListKeyDownCapture,
    isLoading,
    isLoadingAccountArticles,
    isLoadingTagArticles,
    isSearchLoading,
    isSearchEmptyState,
    trimmedDebouncedQuery,
    articleGroups,
    dimArchived,
    textPreview,
    imagePreviews,
    selectionStyle,
    selectArticle,
    handleCloseSearch,
    handleMarkAllRead,
  });

  return {
    layoutMode,
    headerProps: {
      showSearch,
      searchQuery,
      searchInputRef,
      showSidebarButton,
      sidebarButtonLabel,
      sidebarButtonText,
      isSidebarVisible,
      feedModeControl,
      onMarkAllRead: handleMarkAllRead,
      onToggleSidebar: handleSidebarToggle,
      onToggleSearch: handleToggleSearch,
      onCloseSearch: handleCloseSearch,
      onSearchQueryChange: setSearchQuery,
    },
    contextStripProps: {
      primaryLabel: contextStripContext.primaryLabel,
      secondaryLabel: contextStripContext.secondaryLabel,
      tone: contextStripContext.tone,
    },
    bodyProps,
    footerProps: {
      viewMode: effectiveViewMode,
      modes: footerModes,
      onSetViewMode: setViewMode,
    },
  };
}
